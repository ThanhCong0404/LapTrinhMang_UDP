    public frmCLient() {
        initComponents();


         try {
            client = new Client(8000);

            new Thread(() -> {
                while(true){
                    try {
                        Map<String,Integer> dataNhan = (Map<String, Integer>) client.receive();

                        String strResult = "";
                        for(Map.Entry<String, Integer> data : dataNhan.entrySet()){
                            strResult += "Kí tự : " + data.getKey() + " xuất hiện :" + data.getValue() + " lần\n";
                        }
                        txtKetQua.setText(strResult);
                    } catch (IOException | ClassNotFoundException e) {
                        e.printStackTrace();
                    }

                }
            }).start();
        } catch (Exception e) {
             e.printStackTrace();
        }
    }


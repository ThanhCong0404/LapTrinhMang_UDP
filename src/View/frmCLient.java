    private void btnGiaiMaActionPerformed(java.awt.event.ActionEvent evt) {
        //validate
        if(txtVanBan.getText().equals("") || txtKhoa.getText().length()<1){
            JOptionPane.showMessageDialog(this,"Văn bản hoặc khóa chưa được nhập !!!");
            return;
        }
        // TODO add your handling code here:
        String textInput = txtVanBan.getText();
        int Khoa = Integer.parseInt(txtKhoa.getText());

        txtKetQua.setText (CaesarCipher.decrypt(textInput,Khoa).toString());
    }

    private void btnMaHoaActionPerformed(java.awt.event.ActionEvent evt) {
        //validate
        if(txtVanBan.getText().equals("") || txtKhoa.getText().length()<1){
            JOptionPane.showMessageDialog(this,"Văn bản hoặc khóa chưa được nhập !!!");
            return;
        }
        // TODO add your handling code here:
        Map<Integer, String> map = new HashMap<>();

        String textInput = txtVanBan.getText();
        int khoa = Integer.parseInt(txtKhoa.getText());
        String ketQua= CaesarCipher.encrypt(textInput,khoa).toString();

        map.put(khoa, ketQua);

        txtKetQua.setText (ketQua);

    }

    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        //validate
        if(txtVanBan.getText().equals("") || txtKhoa.getText().length()<1 ){
            JOptionPane.showMessageDialog(this,"Văn bản hoặc khóa chưa được nhập !!!");
            return;
        }

        ////////////////
        Map<Integer, String> map = new HashMap<>();

        String textInput = txtVanBan.getText();
        int khoa = Integer.parseInt(txtKhoa.getText());
        String ketQua= CaesarCipher.encrypt(textInput,khoa).toString();

        map.put(khoa, ketQua);
        try {
            client.send(map);
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
    }

